# 2차원(3 * 4)의 배열을 생성한다.
array( 1:12, dim=c(3,4) )
#       [,1] [,2] [,3] [,4]
# [1,]    1    4    7   10
# [2,]    2    5    8   11
# [3,]    3    6    9   12

# 3차원(2 * 2 * 3)의 배열을 생성한다.
(x <- array( 1:12, dim=c(2,2,3) ))
# , , 1 <-- 콤마 2개 이후에 숫자 1은 1열을 의미한다.
# 
#      [,1] [,2] <-- 콤마 1개 이후에 숫자 1과 2는 행을 의미한다.
# [1,]    1    3 <-- 1면
# [2,]    2    4 <-- 2면
# 
# , , 2
# 
#      [,1] [,2]
# [1,]    5    7
# [2,]    6    8
# 
# , , 3
# 
#      [,1] [,2]
# [1,]    9   11
# [2,]   10   12

x[1, 1, 1]
# [1] 1

x[1, 2, 3]
# [1] 11

x[, , 3]
#      [,1] [,2]
# [1,]    9   11
# [2,]   10   12

dim(x) # 2면 2행 3열의 차원을 가진다.
# [1] 2 2 3